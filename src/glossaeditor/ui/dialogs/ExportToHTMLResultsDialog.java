    private void centerWindowOnFrame(Frame parent) {
        Rectangle abounds = getBounds();
        int x = (parent.getWidth() - abounds.width) / 2;
        int y = (parent.getHeight() - abounds.height) / 2;
        setLocation(x, y);
        setLocationRelativeTo(parent);        
        requestFocus();
    }

    private void openBrowser() {
        showFileInBrowser(this.htmlFile);
    }

    public void showFileInBrowser(File f) {
        try {
            java.awt.Desktop desktop = java.awt.Desktop.getDesktop();
            desktop.open(f);
        } catch (UnsupportedOperationException uoe) {
            this.xdgOpenFile(f);
        } catch (Exception e) {
            System.err.println(e.toString());

        }
    }

    private void xdgOpenFile(File f) {
        String CROSS_DESKTOP_OPEN_FILE_COMMAND = "/usr/bin/xdg-open";
        if (this.osName.toLowerCase().equals("linux")) {
            try {
                File parentDir = f.getParentFile();
                String fname = f.getName();
                String cmd = "/bin/sh " + CROSS_DESKTOP_OPEN_FILE_COMMAND + " " + fname;
                Runtime rt = Runtime.getRuntime();
                Process p = rt.exec(cmd, null, parentDir);
                p.waitFor();
            } catch (Exception e) {
            }
        }
    }

